  <h1 class="text-lg sm:text-xl xl:text-4xl font-bold">Start saving your Time ⏳</h1>

  <p>
    Zen addresses a cognitive vulnerability that causes you to start random tasks shortly before the one you intended (e.g. you planned to write your thesis, but five minutes before starting you opened Instagram for two hours 😵‍💫).
    <br>
    This doesn't mean it prevents you from engaging in “non-productive” activities. If you want to scroll through TikTok for four hours, that's fine, just schedule it in Zen and you're good to go.
  </p>
  <p>
    It operates on <b>three</b> routine tasks that you must follow rigorously:
  </p>

  <ol class="flex flex-col gap-4 pl-4 sm:pl-12 list-decimal list-outside marker:font-bold">
    <li> 
      Each morning after waking up, open the <a href="/planner" class="underline">Zen Planner</a> 🗓️ and map out your entire day using events (similar to a standard calendar).
      <div class="glass p-4 my-4 rounded-xl text-violet-500/60">
        <b>TIP</b> If it suits you, aim to make your plan as precise and detailed as possible.
      </div>
    </li>
    <li> 
      <p>Once planned, your day is governed by Zen. Before beginning or ending any activity, always open the <a href="/timer" class="underline">Zen Timer</a> ⏱️ and record it accordingly.</p>
      <div class="glass p-4 my-4 rounded-xl text-red-800/80">
        <b class="animate-pulse">CAUTION</b> For Zen to function correctly, establish a strict mental boundary that prevents you from falsifying start or stop actions. Misusing these (e.g. pressing start without actually starting) can create a lasting cognitive vulnerability that renders Zen ineffective.
        <br>
        If, for any reason, you need to depart from the plan, see <b>3.</b> but <b>NEVER</b> misuse start or stop!
      </div>
    </li>
    <li> 
      <p>For unplanned or spontaneous changes, open the <a href="/planner" class="underline">Zen Planner</a> 🗓️ again and adjust your schedule.</p>
      <div class="glass p-4 my-4 rounded-xl text-red-800/80">
        <b class="animate-pulse">CAUTION</b> Zen functions only if you refrain from altering your plan arbitrarily without a substantial external cause!
      </div>
    </li>
  </ol>

  <p>
    By following these steps, Zen guides you through the day and provides rated feedback 📊 that rewards strict adherence to your plan (especially when it's precise and detailed).
    <br>
    The <a href="/leaderboard" class="underline">Zen Leaderboard</a> 🏆 compares your rating with others; treat it lightly - it serves mainly as a reference to measure your relative consistency.
  </p>

  <div class="glass p-4 my-4 rounded-xl text-red-800/80">
    <b class="animate-pulse">CAUTION</b> Once you begin using Zen, it's essential to comply fully and let it structure your entire day.
    Do not take this commitment lightly! If you're uncertain about maintaining it, <b>DO NOT USE ZEN</b>, as improper use can permanently impair your connection with the system.
